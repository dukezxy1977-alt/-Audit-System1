import json
import os

RULE_OVERRIDES_FILE = os.path.join(os.path.dirname(os.path.abspath(__file__)), 'data', 'rule_overrides.json')


class RiskRules:
    """招标投标风险评估规则引擎 - 包含 8 个模块、101 项风险指标"""

    def __init__(self):
        self.rules = self._initialize_rules()
        self._apply_overrides()

    def _apply_overrides(self):
        """
        加载人工复核异议后产生的规则修正配置（data/rule_overrides.json）。
        这个文件只应该由 manage_disputes.py 在人工确认某条异议成立后追加写入，
        rules_engine 本身只读不写，避免规则被静默、自动地改写。
        """
        if not os.path.exists(RULE_OVERRIDES_FILE):
            return

        try:
            with open(RULE_OVERRIDES_FILE, 'r', encoding='utf-8') as f:
                overrides = json.load(f)
        except (json.JSONDecodeError, OSError):
            return

        for rule_id in overrides.get('disabled_rules', []):
            self.rules.pop(rule_id, None)

        for rule_id, keywords in overrides.get('removed_keywords', {}).items():
            if rule_id in self.rules:
                self.rules[rule_id]['keywords'] = [
                    kw for kw in self.rules[rule_id]['keywords'] if kw not in keywords
                ]

        for rule_id, keywords in overrides.get('added_keywords', {}).items():
            if rule_id in self.rules:
                existing = self.rules[rule_id]['keywords']
                for kw in keywords:
                    if kw not in existing:
                        existing.append(kw)

